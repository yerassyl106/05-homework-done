// 1. Пайдаланушыдан жасын сұрау
let age = prompt("Жасыңызды енгізіңіз:");

// Жасқа байланысты хабарлама көрсету
if (age >= 18) {
    alert("Сіз ересексіз!");
} else {
    alert("Сіз кәмелетке толмағансыз!");
}

// 2. Пайдаланушыдан температураны сұрау
let temperature = prompt("Даладағы температураны енгізіңіз:");

// Температураға байланысты киім ұсыну
if (temperature > 25) {
    alert("Жеңіл шорты және футболка киіңіз!");
} else if (temperature >= 0) {
    alert("Жеңіл күртка немесе жемпір киіңіз!");
} else {
    alert("Қалың куртка мен жемпір киіңіз, ауа райы өте суық!");
}
